package com.linuxtips.descomplicandojavaspring.variaveisEtiposdeDados;

public class Operadores {
    public static void main(String []args){
        //Operador de atribuição
        int idade = 25;
        System.out.println( "Valor atribuido a idade "+idade);

        //operadores matematicos
        int soma = idade+5;
        System.out.println("resultado da soma "+soma);
        int multiplicacao = idade*5;
        System.out.println("resultado da multiplicacao "+multiplicacao);
        int divisao = idade/5;
        System.out.println("resultado da divisao "+divisao);
        int subtracao = idade-5;
        System.out.println("resultado da subtracao "+subtracao);

    }
}
